@dataclass
class Alert:
    """Automatic alert generated by the system."""
    alert_type: str  # suspicious_route_match, pattern_drift, recurring_route
    plate_number: str
    severity: str  # low, medium, high, critical
    confidence: float
    details: dict
    triggered_at: datetime
    requires_review: bool
    targets: List[str] = field(default_factory=lambda: ["mobile_agents"])


